export const getTranslation = (key: string, lang: 'zh' | 'en', params?: Record<string, string | number>) => {
    let text = TRANSLATIONS[lang]?.[key] || key;
    if (params) {
        Object.entries(params).forEach(([k, v]) => {
            text = text.replace(`{{${k}}}`,String(v));
        });
    }
    return text;
};
